def fill_template(tpl, variant):
    values = {}
    for n, (key, arr) in enumerate(tpl.get('vars', {}).items()):
        values[key] = arr[(variant + n * 7) % len(arr)]
    return tpl['pattern'].format(**values)

FORBIDDEN = [" — ", "семейный опрос", "уличный опрос", "быстрый раунд", "блиц"]
BAD_COMBOS = [
    ("холодильник", "Telegram"),
    ("холодильнике", "Telegram"),
    ("спортзале", "семей"),
]

def make_question(gid: int):
    cat = CATEGORIES[gid % len(CATEGORIES)]
    templates = TEMPLATES[cat['id']]
    local = gid // len(CATEGORIES)
    tpl = templates[local % len(templates)]
    variant = local // len(templates)
    q = fill_template(tpl, variant)
    if not q.endswith('?'):
        q += '?'
    for bad in FORBIDDEN:
        if bad in q:
            raise ValueError(f'Forbidden phrase {bad}: {q}')
    for a, b in BAD_COMBOS:
        if a.lower() in q.lower() and b.lower() in q.lower():
            raise ValueError(f'Bad combo: {q}')
    answers = [[txt, score] for txt, score in zip(tpl['answers'], SCORES)]
    return {"id": gid + 1, "c": cat['id'], "q": q, "a": answers}

